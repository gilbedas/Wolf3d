#include "../includes/libft.h"

int	ft_divcount(int c)
{
	int i;
	int b;
	int j;

	j = 0;
	i = 1;
	b = c / 2;
	while (i <= b)
	{
		if (c % i == 0)
		{
			j++;
		}
		i++;
	}
	if (c != 1)
	{
		j++;
	}
	return (j);
}
